function processIsRunning(pid) {
  if (!Number.isInteger(pid) || pid <= 0) return false;
  try { process.kill(pid, 0); return true; } catch { return false; }
}

function acquireSchemaLock() {
  ensureDirs();
  try {
    const existing = JSON.parse(fs.readFileSync(SCHEMA_LOCK_PATH, "utf8"));
    const ageMs = Date.now() - Date.parse(existing.startedAt || 0);
    if (processIsRunning(Number(existing.pid)) && ageMs < 15 * 60 * 1000) {
      fail("Home OS setup is already running", { pid: existing.pid, startedAt: existing.startedAt });
    }
    fs.unlinkSync(SCHEMA_LOCK_PATH);
  } catch (error) {
    if (error.code !== "ENOENT" && error.name !== "SyntaxError") throw error;
    if (error.name === "SyntaxError") fs.unlinkSync(SCHEMA_LOCK_PATH);
  }
  const lock = { pid: process.pid, startedAt: new Date().toISOString() };
  const fd = fs.openSync(SCHEMA_LOCK_PATH, "wx", 0o600);
  fs.writeFileSync(fd, `${JSON.stringify(lock)}\n`);
  fs.closeSync(fd);
  return () => {
    try {
      const current = JSON.parse(fs.readFileSync(SCHEMA_LOCK_PATH, "utf8"));
      if (current.pid === process.pid) fs.unlinkSync(SCHEMA_LOCK_PATH);
    } catch {}
  };
}

async function applySchemaUnlocked(config, confirmation) {
  const initialPlan = await schemaPlan(config);
  if (!initialPlan.ok) fail("Home OS schema has unresolved collisions", { conflicts: initialPlan.conflicts });
  if (!confirmation || confirmation !== initialPlan.applyToken) {
    fail("Schema confirmation does not match the current read-only plan", { requiredConfirmation: initialPlan.applyToken, next: `schema-apply --confirm ${initialPlan.applyToken}` });
  }

  const backup = await saveSchemaBackup(config);
  const receipt = {
    ok: false,
    operation: "home-os-schema-apply",
    graph: initialPlan.graph,
    schemaVersion: HOME_OS_SCHEMA_VERSION,
    planToken: initialPlan.applyToken,
    startedAt: new Date().toISOString(),
    backup,
    checkpoints: [],
  };
  const auditDirectory = path.join(config.graphPath, ".logseq-agent-audit");
  fs.mkdirSync(auditDirectory, { recursive: true, mode: 0o700 });
  const receiptPath = path.join(auditDirectory, `home-os-schema-v${HOME_OS_SCHEMA_VERSION}-${timestampForFilename()}.json`);

  try {
    for (const tag of initialPlan.tags.filter((item) => item.action === "create")) await rpc(config, "logseq.Editor.createTag", [tag.title]);
    receipt.checkpoints.push({ step: "tags-created-or-reused", at: new Date().toISOString() });
    let tags = await rpc(config, "logseq.Editor.getAllTags", []);
    let properties = await rpc(config, "logseq.Editor.getAllProperties", []);
    let tagByTitle = new Map(tags.map((tag) => [entityTitle(tag), tag]));

    for (const property of HOME_OS_PROPERTIES) {
      await rpc(config, "logseq.Editor.upsertProperty", [
        property.key,
        { type: property.type, cardinality: property.cardinality, hide: Boolean(property.hide) },
        { name: property.label },
      ]);
    }
    properties = await rpc(config, "logseq.Editor.getAllProperties", []);
    let propertyByKey = new Map(properties.map((property) => [propertyStableKey(property), property]).filter(([key]) => key));
    for (const propertySpec of HOME_OS_PROPERTIES) {
      const property = propertyByKey.get(propertySpec.key);
      if (!property?.uuid) fail("Could not resolve property after setup", { property: propertySpec.key });
      if (propertyDisplayName(property) !== propertySpec.label) {
        await rpc(config, "logseq.Editor.updateBlock", [property.uuid, propertySpec.label]);
      }
    }
    receipt.checkpoints.push({ step: "properties-created-reused-and-labeled", at: new Date().toISOString() });

    tags = await rpc(config, "logseq.Editor.getAllTags", []);
    properties = await rpc(config, "logseq.Editor.getAllProperties", []);
    tagByTitle = new Map(tags.map((tag) => [entityTitle(tag), tag]));
    propertyByKey = new Map(properties.map((property) => [propertyStableKey(property), property]).filter(([key]) => key));
    const assetTag = await rpc(config, "logseq.Editor.getTag", [":logseq.class/Asset"]);

    for (const propertySpec of HOME_OS_PROPERTIES) {
      if (!propertySpec.range) continue;
      const property = propertyByKey.get(propertySpec.key);
      const rangeTag = propertySpec.range === "Asset" ? assetTag : tagByTitle.get(propertySpec.range);
      if (!property?.uuid || !rangeTag?.id) {
        fail("Could not resolve property range for setup", { property: propertySpec.key, range: propertySpec.range });
      }
      await rpc(config, "logseq.Editor.setPropertyNodeTags", [property.uuid, [rangeTag.id]]);
    }
    receipt.checkpoints.push({ step: "property-ranges-set", at: new Date().toISOString() });

    for (const [tagTitle, propertyKeys] of Object.entries(HOME_OS_TAG_PROPERTIES)) {
      const tag = tagByTitle.get(tagTitle);
      if (!tag?.uuid) fail("Could not resolve tag for property setup", { tag: tagTitle });
      for (const propertyKey of propertyKeys) {
        const property = propertyByKey.get(propertyKey);
        if (!property?.uuid) fail("Could not resolve property for tag setup", { tag: tagTitle, property: propertyKey });
        await rpc(config, "logseq.Editor.addTagProperty", [tag.uuid, propertyKey]);
      }
    }
    receipt.checkpoints.push({ step: "tag-properties-set", at: new Date().toISOString() });

    const preparedPlan = await schemaPlan(config);
    const clientOperations = preparedPlan.inheritance
      .filter((item) => item.action === "add")
      .map(({ child, parent }) => ({ operation: "add-tag-extends", child, parent }));
    receipt.phase = clientOperations.length ? "awaiting-logseq-plugin" : "prepared";
    receipt.preparedAt = new Date().toISOString();
    receipt.clientOperations = clientOperations;
    fs.writeFileSync(receiptPath, `${JSON.stringify(receipt, null, 2)}\n`, { mode: 0o600 });
    return { ...receipt, receiptPath, clientOperations };
  } catch (error) {
    receipt.failedAt = new Date().toISOString();
    receipt.error = error.message;
    receipt.details = error.details;
    fs.writeFileSync(receiptPath, `${JSON.stringify(receipt, null, 2)}\n`, { mode: 0o600 });
    error.details = { ...(error.details && typeof error.details === "object" ? error.details : { cause: error.details }), backup, receiptPath };
    throw error;
  }
}

async function applySchema(config, confirmation) {
  const release = acquireSchemaLock();
  try {
    return await applySchemaUnlocked(config, confirmation);
  } finally {
    release();
  }
}

