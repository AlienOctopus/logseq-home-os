function restorePreparedSetupSession(config) {
  const directory = path.join(config.graphPath, ".logseq-agent-audit");
  let filenames;
  try {
    filenames = fs.readdirSync(directory)
      .filter((name) => name.startsWith(`home-os-schema-v${HOME_OS_SCHEMA_VERSION}-`) && name.endsWith(".json"))
      .sort()
      .reverse();
  } catch (error) {
    if (error.code === "ENOENT") return null;
    throw error;
  }
  for (const filename of filenames) {
    const receiptPath = path.join(directory, filename);
    const receipt = loadJson(receiptPath, null);
    if (receipt?.graph !== graphStateKey(config) || receipt?.phase !== "awaiting-logseq-plugin") continue;
    if (!receipt.backup?.path || !Array.isArray(receipt.clientOperations) || receipt.clientOperations.length === 0) continue;
    return {
      token: randomBytes(24).toString("hex"),
      receiptPath,
      backup: receipt.backup,
      clientOperations: receipt.clientOperations,
      restored: true,
    };
  }
  return null;
}

async function serve() {
  const config = loadConfig();
  const interruptedRunCleanup = cleanupInterruptedCodexRun();
  let timer = null;
  let running = false;
  let setupRunning = false;
  let setupSession = restorePreparedSetupSession(config);
  let pending = false;
  let pendingReason = "db-changed";
  let scheduledRunId = null;
  let activeRun = null;
  let lastRun = null;

  if (interruptedRunCleanup.terminatedPid || interruptedRunCleanup.removedLogs.length) {
    await audit({
      type: "interrupted-codex-run-cleanup",
      terminatedPid: interruptedRunCleanup.terminatedPid,
      removedLogCount: interruptedRunCleanup.removedLogs.length,
    });
  }
  if (setupSession) {
    await audit({
      type: "setup-session-restored",
      receiptPath: setupSession.receiptPath,
      operations: setupSession.clientOperations,
    });
  }

  const processEvent = async () => {
    if (running || setupRunning) { pending = true; return; }
    running = true;
    const runId = scheduledRunId ?? randomBytes(8).toString("hex");
    scheduledRunId = null;
    const reason = pendingReason;
    activeRun = {
      runId,
      reason,
      phase: "scanning",
      captureCount: 0,
      startedAt: new Date().toISOString(),
    };
    const completeRun = (result) => {
      lastRun = {
        ...activeRun,
        ...result,
        finishedAt: new Date().toISOString(),
      };
      activeRun = null;
    };
    try {
      const captures = await scanCaptures(config);
      const changed = captures.filter((capture) => capture.changed);
      activeRun.captureCount = changed.length;
      await audit({ type: "scan", runId, changed: changed.map((capture) => capture.uuid), total: captures.length, reason });
      if (changed.length === 0) {
        completeRun({ outcome: "up_to_date", handledCount: 0, message: "No new captures." });
        return;
      }
      const schema = await schemaReadiness(config);
      if (!schema.ready) {
        await audit({ type: "setup-required", runId, captures: changed.map((capture) => capture.uuid), missing: schema.missing, reason });
        completeRun({ outcome: "setup_required", handledCount: 0, message: "Home OS setup is required." });
        return;
      }
      const isManual = reason === "manual";
      if (!isManual && !config.autoRun) {
        await audit({ type: "queued", runId, captures: changed.map((capture) => capture.uuid), reason });
        completeRun({ outcome: "waiting_for_manual_run", handledCount: 0, message: `${changed.length} capture${changed.length === 1 ? "" : "s"} waiting.` });
        return;
      }
      activeRun.phase = "processing";
      const result = await runCodex(config, changed, reason);
      await audit({ type: "codex-run", runId, captures: changed.map((capture) => capture.uuid), ...result });
      if (result.ok) await markHandled(config, changed, result.handledCaptureUuids);
      completeRun({
        outcome: result.complete ? "processed" : result.ok ? "partially_processed" : "failed",
        handledCount: result.handledCaptureUuids?.length ?? 0,
        missingCount: result.missingCaptureUuids?.length ?? changed.length,
        message: result.complete
          ? `${changed.length} capture${changed.length === 1 ? "" : "s"} processed.`
          : result.ok
            ? "Some captures still need attention."
            : "Processing stopped safely.",
      });
    } catch (error) {
      await audit({ type: "error", runId, error: error.message, details: error.details });
      completeRun({ outcome: "failed", handledCount: 0, message: "Processing stopped safely." });
    } finally {
      if (activeRun?.runId === runId) completeRun({ outcome: "failed", handledCount: 0, message: "Processing stopped safely." });
      running = false;
      if (pending) {
        pending = false;
        setTimeout(processEvent, 250);
      }
    }
  };

  const schedule = (reason) => {
    pendingReason = reason;
    scheduledRunId = randomBytes(8).toString("hex");
    if (timer) clearTimeout(timer);
    timer = setTimeout(processEvent, reason === "manual" ? 50 : config.debounceMs);
    return scheduledRunId;
  };

  const authorized = (request) => request.headers.authorization === `Bearer ${config.bridgeSecret}`;
  const readBody = async (request) => {
    let body = "";
    for await (const chunk of request) {
      body += chunk;
      if (body.length > 8192) fail("Request body is too large");
    }
    try { return body ? JSON.parse(body) : {}; } catch { fail("Request body is not valid JSON"); }
  };

  const publicPlan = (plan) => ({
    ok: plan.ok,
    summary: plan.summary,
    conflicts: plan.conflicts.map((conflict) => ({
      kind: conflict.kind,
      title: conflict.title,
      key: conflict.key,
      reason: conflict.reason,
      expected: conflict.expected,
      actual: conflict.actual,
    })),
  });

  const handleRequest = async (request, response) => {
    response.setHeader("Access-Control-Allow-Origin", "*");
    response.setHeader("Access-Control-Allow-Headers", "Authorization, Content-Type");
    if (request.method === "OPTIONS") { response.writeHead(204); response.end(); return; }
    if (request.url === "/health" && request.method === "GET") {
      response.writeHead(200, { "Content-Type": "application/json" });
      response.end(JSON.stringify({
        ok: true,
        running: true,
        autoRun: config.autoRun,
        busy: running || setupRunning,
        processing: running,
        setupRunning,
        awaitingLogseqPlugin: Boolean(setupSession),
        activeRun,
        lastRun,
      }));
      return;
    }
    if (request.url === "/dashboard" && request.method === "POST") {
      if (!authorized(request)) {
        response.writeHead(401); response.end("Unauthorized"); return;
      }
      const schema = await schemaReadiness(config);
      if (!schema.ready) {
        const captures = await scanCaptures(config);
        const changed = captures.filter((capture) => capture.changed);
        const plan = await schemaPlan(config);
        response.writeHead(200, { "Content-Type": "application/json" });
        response.end(JSON.stringify({
          ok: false,
          setupRequired: true,
          pendingCaptureCount: changed.length,
          plan: publicPlan(plan),
        }));
        return;
      }
      const dashboard = await ensureDashboard(config);
      response.writeHead(200, { "Content-Type": "application/json" });
      response.end(JSON.stringify({ ok: true, dashboard }));
      return;
    }
    if (request.url === "/events" && request.method === "POST") {
      if (!authorized(request)) {
        response.writeHead(401); response.end("Unauthorized"); return;
      }
      let event;
      try { event = await readBody(request); } catch (error) { response.writeHead(400); response.end(JSON.stringify({ error: error.message })); return; }
      const reason = event.reason === "manual" ? "manual" : "db-changed";
      await audit({ type: "event", reason });
      if (reason === "manual") {
        if (running || setupRunning) {
          response.writeHead(200, { "Content-Type": "application/json" });
          response.end(JSON.stringify({ ok: true, busy: true, processing: running, setupRunning, activeRun }));
          return;
        }
        const schema = await schemaReadiness(config);
        if (!schema.ready) {
          const captures = await scanCaptures(config);
          const changed = captures.filter((capture) => capture.changed);
          const plan = await schemaPlan(config);
          await audit({ type: "setup-required", missing: schema.missing, conflicts: plan.conflicts, captures: changed.map((capture) => capture.uuid), reason });
          response.writeHead(200, { "Content-Type": "application/json" });
          response.end(JSON.stringify({
            ok: false,
            setupRequired: true,
            pendingCaptureCount: changed.length,
            plan: publicPlan(plan),
            reason,
          }));
          return;
        }
        const captures = await scanCaptures(config);
        const changed = captures.filter((capture) => capture.changed);
        if (changed.length === 0) {
          const runId = randomBytes(8).toString("hex");
          lastRun = {
            runId,
            reason,
            phase: "complete",
            captureCount: 0,
            handledCount: 0,
            outcome: "up_to_date",
            message: "No new captures.",
            startedAt: new Date().toISOString(),
            finishedAt: new Date().toISOString(),
          };
          await audit({ type: "scan", runId, changed: [], total: captures.length, reason });
          response.writeHead(200, { "Content-Type": "application/json" });
          response.end(JSON.stringify({ ok: true, queued: false, runId, changedCount: 0, outcome: "up_to_date" }));
          return;
        }
        const runId = schedule(reason);
        response.writeHead(202, { "Content-Type": "application/json" });
        response.end(JSON.stringify({ ok: true, queued: true, runId, changedCount: changed.length, reason }));
        return;
      }
      const runId = schedule(reason);
      response.writeHead(202, { "Content-Type": "application/json" });
      response.end(JSON.stringify({ ok: true, queued: true, runId, reason }));
      return;
    }
    if (request.url === "/setup" && request.method === "POST") {
      if (!authorized(request)) {
        response.writeHead(401); response.end("Unauthorized"); return;
      }
      let event;
      try { event = await readBody(request); } catch (error) { response.writeHead(400, { "Content-Type": "application/json" }); response.end(JSON.stringify({ error: error.message })); return; }
      if (event.action !== "install-and-process") {
        response.writeHead(400, { "Content-Type": "application/json" });
        response.end(JSON.stringify({ error: "Unsupported setup action" }));
        return;
      }
      if (setupRunning || running) {
        response.writeHead(409, { "Content-Type": "application/json" });
        response.end(JSON.stringify({ error: "Home OS is already working", message: "Home OS is already working. Try again in a moment." }));
        return;
      }

      if (setupSession) {
        response.writeHead(202, { "Content-Type": "application/json" });
        response.end(JSON.stringify({
          ok: true,
          setupCompleted: false,
          clientSetupRequired: true,
          setupToken: setupSession.token,
          clientOperations: setupSession.clientOperations,
          backup: setupSession.backup,
        }));
        return;
      }

      setupRunning = true;
      try {
        const status = await schemaReadiness(config);
        let installation = null;
        if (!status.ready) {
          const plan = await schemaPlan(config);
          if (!plan.ok) {
            await audit({ type: "setup-blocked", conflicts: plan.conflicts });
            response.writeHead(409, { "Content-Type": "application/json" });
            response.end(JSON.stringify({ error: "Schema collision", message: "Setup found an existing name or property it will not overwrite.", plan: publicPlan(plan) }));
            return;
          }
          await audit({ type: "setup-started", planToken: plan.applyToken, summary: plan.summary });
          installation = await applySchema(config, plan.applyToken);
          if (installation.clientOperations.length) {
            setupSession = {
              token: randomBytes(24).toString("hex"),
              receiptPath: installation.receiptPath,
              backup: installation.backup,
              clientOperations: installation.clientOperations,
            };
            await audit({
              type: "setup-awaiting-logseq-plugin",
              receiptPath: installation.receiptPath,
              operations: installation.clientOperations,
            });
            response.writeHead(202, { "Content-Type": "application/json" });
            response.end(JSON.stringify({
              ok: true,
              setupCompleted: false,
              clientSetupRequired: true,
              setupToken: setupSession.token,
              clientOperations: setupSession.clientOperations,
              backup: setupSession.backup,
            }));
            return;
          }
          installation = await completePreparedSchema(config, installation);
          await audit({ type: "setup-completed", receiptPath: installation.receiptPath, backup: installation.backup, choiceMode: installation.choiceMode });
        }
        const verification = await schemaVerification(config);
        if (!verification.ok) fail("Home OS setup verification did not pass", verification);
        const dashboard = await ensureDashboard(config);
        const captures = await scanCaptures(config);
        const changed = captures.filter((capture) => capture.changed);
        const processingRunId = changed.length > 0 ? schedule("manual") : null;
        response.writeHead(200, { "Content-Type": "application/json" });
        response.end(JSON.stringify({
          ok: true,
          setupCompleted: true,
          alreadyReady: status.ready,
          backup: installation?.backup ?? null,
          dashboard,
          pendingCaptureCount: changed.length,
          processingQueued: changed.length > 0,
          processingRunId,
        }));
      } catch (error) {
        await audit({ type: "setup-error", error: error.message, details: error.details });
        response.writeHead(500, { "Content-Type": "application/json" });
        response.end(JSON.stringify({ error: "Setup failed", message: "Setup stopped safely. Your captures were not changed." }));
      } finally {
        setupRunning = false;
        if (pending) {
          pending = false;
          schedule("manual");
        }
      }
      return;
    }
    if (request.url === "/setup/complete" && request.method === "POST") {
      if (!authorized(request)) {
        response.writeHead(401); response.end("Unauthorized"); return;
      }
      let event;
      try { event = await readBody(request); } catch (error) { response.writeHead(400, { "Content-Type": "application/json" }); response.end(JSON.stringify({ error: error.message })); return; }
      if (!setupSession || event.setupToken !== setupSession.token) {
        response.writeHead(409, { "Content-Type": "application/json" });
        response.end(JSON.stringify({ error: "Setup session expired", message: "Setup can be resumed safely from the Home OS button." }));
        return;
      }
      if (setupRunning || running) {
        response.writeHead(409, { "Content-Type": "application/json" });
        response.end(JSON.stringify({ error: "Home OS is already working", message: "Home OS is already working. Try again in a moment." }));
        return;
      }
      setupRunning = true;
      try {
        const installation = await completePreparedSchema(config, setupSession);
        await audit({ type: "setup-completed", receiptPath: installation.receiptPath, backup: installation.backup, choiceMode: installation.choiceMode });
        setupSession = null;
        const dashboard = await ensureDashboard(config);
        const captures = await scanCaptures(config);
        const changed = captures.filter((capture) => capture.changed);
        const processingRunId = changed.length > 0 ? schedule("manual") : null;
        response.writeHead(200, { "Content-Type": "application/json" });
        response.end(JSON.stringify({
          ok: true,
          setupCompleted: true,
          alreadyReady: false,
          backup: installation.backup,
          choiceMode: installation.choiceMode,
          dashboard,
          pendingCaptureCount: changed.length,
          processingQueued: changed.length > 0,
          processingRunId,
        }));
      } catch (error) {
        await audit({ type: "setup-error", error: error.message, details: error.details });
        response.writeHead(500, { "Content-Type": "application/json" });
        response.end(JSON.stringify({ error: "Setup failed", message: "Setup stopped safely. Your captures were not changed." }));
      } finally {
        setupRunning = false;
      }
      return;
    }
    response.writeHead(404); response.end("Not found");
  };

  const server = http.createServer((request, response) => {
    void handleRequest(request, response).catch(async (error) => {
      try { await audit({ type: "bridge-request-error", method: request.method, url: request.url, error: error.message, details: error.details }); } catch {}
      if (response.destroyed || response.writableEnded) return;
      const logseqUnavailable = error.details?.message === "Object has been destroyed" || error.message.includes("Logseq API");
      response.writeHead(logseqUnavailable ? 503 : 500, { "Content-Type": "application/json" });
      response.end(JSON.stringify({
        error: logseqUnavailable ? "Logseq is temporarily unavailable" : "Home OS request failed",
        message: logseqUnavailable ? "Logseq is restarting. Your setup and captures are safe; try the dashboard action again in a moment." : "Home OS stopped safely. Your captures were not changed.",
      }));
    });
  });

  server.on("error", (error) => fail("Home OS bridge failed", error.message));
  server.listen(config.bridgePort, config.bridgeHost, () => {
    process.stdout.write(`Home OS bridge listening on http://${config.bridgeHost}:${config.bridgePort}\n`);
  });
}

async function main() {
  const args = parseArgs(process.argv.slice(2));
  const command = args._[0] ?? "help";
  if (command === "configure") return configure(args);
  if (command === "configure-current") return configureCurrent(args);
  if (command === "bootstrap") return bootstrap(args);
  if (command === "doctor") return doctor();
  if (command === "status") return status();
  if (command === "self-test") return selfTest();
  if (command === "trigger") return trigger(args);
  if (command === "setup") return setupAndProcess();
  if (command === "schema-plan") return output(await schemaPlan(loadConfig()));
  if (command === "schema-verify") return output(await schemaVerification(loadConfig()));
  if (command === "schema-apply") return output(await applySchema(loadConfig(), args.confirm));
  if (command === "schema-complete") return output(await completeSchemaReceipt(loadConfig(), args.receipt));
  if (command === "dashboard") return output(await ensureDashboard(loadConfig()));
  if (command === "dashboard-repair") return output(await repairDashboardDuplicates(loadConfig(), args.confirm));
  if (command === "serial-audit") return output(await serialCoverage(loadConfig()));
  if (command === "record-audit") return output(await recordIntegrityAudit(loadConfig()));
  if (command === "privacy-audit") return output(await privacyAudit(loadConfig()));
  if (command === "scan") {
    const captures = await scanCaptures(loadConfig());
    return output({ ok: true, captures: args.changedOnly ? captures.filter((capture) => capture.changed) : captures });
  }
  if (command === "materialize") return materialize(loadConfig(), args.input);
  if (command === "install-logseq-plugin") return installLogseqPlugin();
  if (command === "install-launch-agent") return installLaunchAgent();
  if (command === "install-local") return installLocal();
  if (command === "reinstall-local") return reinstallLocal();
  if (command === "uninstall-logseq-plugin") return uninstallLogseqPlugin();
  if (command === "uninstall-launch-agent") return uninstallLaunchAgent();
  if (command === "uninstall-local") return uninstallLocal(args);
  if (command === "serve") return serve();
  output({
    ok: true,
    usage: [
      "configure --graph-path <path> --graph-name <name> [--auto-run true|false]",
      "configure-current [--auto-run true|false]",
      "bootstrap [--auto-run true|false]",
      "doctor",
      "scan [--changed-only] [--json]",
      "materialize --input <absolute-json-path>",
      "status",
      "self-test",
      "setup",
      "schema-plan",
      "schema-verify",
      "schema-apply --confirm <plan-token>",
      "schema-complete --receipt <absolute-schema-receipt-path>",
      "dashboard",
      "dashboard-repair --confirm remove-duplicate-generated-dashboard-nodes",
      "serial-audit",
      "record-audit",
      "privacy-audit",
      "trigger [--reason manual|db-changed]",
      "install-logseq-plugin",
      "install-launch-agent",
      "install-local",
      "reinstall-local",
      "uninstall-logseq-plugin",
      "uninstall-launch-agent",
      "uninstall-local --confirm remove-home-os-local-runtime",
      "serve",
    ],
  });
}

main().catch((error) => {
  output({ ok: false, error: error.message, details: error.details });
  process.exitCode = 1;
});
