const CAPTURE_QUERY = `[:find (pull ?b [:db/id :block/uuid :block/title :block/created-at :block/updated-at {:block/page [:block/uuid :block/title]} {:block/parent [:block/uuid :block/title]} {:block/tags [:block/title]}])
 :where
 [?tag :block/title "HomeCapture"]
 [?b :block/tags ?tag]]`;

async function scanCaptures(config) {
  await assertGraph(config);
  const rows = await rpc(config, "logseq.DB.datascriptQuery", [CAPTURE_QUERY]);
  const captures = [];
  for (const row of Array.isArray(rows) ? rows : []) {
    const pulled = Array.isArray(row) ? row[0] : row;
    const uuid = valueAt(pulled, ["uuid", "block/uuid", ":block/uuid"]);
    if (!uuid) continue;
    let tree;
    try {
      tree = await rpc(config, "logseq.Editor.getBlock", [uuid, { includeChildren: true }]);
    } catch (error) {
      tree = { uuid, readError: error.message };
    }
    const fingerprints = captureFingerprints(tree);
    captures.push({
      uuid,
      page: valueAt(valueAt(pulled, ["page", "block/page", ":block/page"]), ["title", "block/title", ":block/title"]),
      title: valueAt(pulled, ["title", "block/title", ":block/title"]),
      tree,
      fingerprint: fingerprints.semantic,
      fingerprintAlgorithm: "capture-v2",
      legacyFingerprint: fingerprints.legacy,
    });
  }
  captures.sort((a, b) => a.uuid.localeCompare(b.uuid));
  const { graphState } = loadStateForGraph(config);
  return captures.map((capture) => {
    const handled = graphState.handled?.[capture.uuid];
    return { ...capture, changed: handled !== capture.fingerprint && handled !== capture.legacyFingerprint };
  });
}

function requireText(value, label, { max = 500 } = {}) {
  const text = String(value ?? "").trim();
  if (!text) fail(`${label} is required`);
  if (text.length > max) fail(`${label} is too long`, { max, actual: text.length });
  return text;
}

function optionalText(value, label, { max = 500 } = {}) {
  if (value == null || String(value).trim() === "") return null;
  return requireText(value, label, { max });
}

function optionalHttpUrl(value, label) {
  const text = optionalText(value, label, { max: 2000 });
  if (!text) return null;
  let parsed;
  try { parsed = new URL(text); } catch { fail(`${label} is not a valid URL`); }
  if (!["https:", "http:"].includes(parsed.protocol)) fail(`${label} must use HTTP(S)`);
  return text;
}

function optionalUuid(value, label) {
  const text = optionalText(value, label, { max: 80 });
  if (!text) return null;
  if (!/^[0-9a-f]{8}-[0-9a-f]{4}-[1-8][0-9a-f]{3}-[89ab][0-9a-f]{3}-[0-9a-f]{12}$/i.test(text)) {
    fail(`${label} must be a Logseq block UUID`);
  }
  return text;
}

function propertyEntry(node, key) {
  if (!node || typeof node !== "object") return null;
  return Object.entries(node).find(([candidate]) => candidate === key || candidate.endsWith(`/${key}`)) ?? null;
}

async function propertyText(config, node, key) {
  const entry = propertyEntry(node, key);
  if (!entry) return null;
  const raw = entry[1];
  if (raw == null) return null;
  if (typeof raw === "string") return raw.trim() || null;
  const id = typeof raw === "number" ? raw : valueAt(raw, ["id", "db/id", ":db/id"]);
  if (id == null) {
    const inline = valueAt(raw, ["value", "title", "content", "name", "block/title", ":block/title"]);
    return inline == null ? null : String(inline).trim() || null;
  }
  const valueNode = await rpc(config, "logseq.Editor.getBlock", [id, { includeChildren: false }]);
  const value = valueAt(valueNode, ["fullTitle", "title", "content", "name", "block/title", ":block/title"]);
  return value == null ? null : String(value).trim() || null;
}

async function fillMissingScalarProperty(config, node, key, proposedValue) {
  if (!proposedValue) return { key, outcome: "not-provided" };
  const existing = await propertyText(config, node, key);
  if (!existing) {
    await setProperty(config, node, key, proposedValue);
    return { key, outcome: "added" };
  }
  if (existing === proposedValue) return { key, outcome: "already-matched" };
  if (key === "hm-serial") {
    fail("The proposed serial conflicts with the serial already stored on this Home OS record", {
      recordUuid: node.uuid,
      property: key,
      next: "Keep both readings in the source review, mark the capture Needs review, and do not overwrite either identifier.",
    });
  }
  return { key, outcome: "preserved-existing" };
}

async function referenceIds(config, node, key) {
  const entry = propertyEntry(node, key);
  if (!entry) return [];
  const ids = [];
  for (const value of propertyReferenceValues(entry[1])) {
    if (typeof value === "number") {
      ids.push(value);
      continue;
    }
    const inlineId = valueAt(value, ["id", "db/id", ":db/id"]);
    if (inlineId != null) {
      ids.push(inlineId);
      continue;
    }
    if (typeof value !== "string") continue;
    const referenced = /^[0-9a-f-]{36}$/i.test(value)
      ? await rpc(config, "logseq.Editor.getBlock", [value, { includeChildren: false }])
      : await rpc(config, "logseq.Editor.getPage", [value]);
    const resolvedId = valueAt(referenced, ["id", "db/id", ":db/id"]);
    if (resolvedId != null) ids.push(resolvedId);
  }
  return [...new Set(ids)];
}

async function fillMissingSingleReference(config, node, key, target, label) {
  if (!target) return { key, outcome: "not-provided" };
  const targetId = valueAt(target, ["id", "db/id", ":db/id"]);
  if (targetId == null) fail(`Could not resolve ${label}`, { key });
  const existingIds = await referenceIds(config, node, key);
  if (existingIds.length === 0) {
    await setProperty(config, node, key, targetId);
    return { key, outcome: "added" };
  }
  if (existingIds.length === 1 && existingIds[0] === targetId) return { key, outcome: "already-matched" };
  fail(`Home OS will not overwrite an existing ${label}`, {
    recordUuid: node.uuid,
    property: key,
    next: "Review the existing relationship in Logseq and change it deliberately before reprocessing this capture.",
  });
}

async function appendReference(config, node, key, target, label) {
  const targetId = valueAt(target, ["id", "db/id", ":db/id"]);
  if (targetId == null) fail(`Could not resolve ${label}`, { key });
  const existingIds = await referenceIds(config, node, key);
  if (existingIds.includes(targetId)) return { key, outcome: "already-matched" };
  await setProperty(config, node, key, [...existingIds, targetId], { reset: true });
  return { key, outcome: "added" };
}

function textList(value, label, { maxItems = 40, max = 1200 } = {}) {
  if (value == null) return [];
  if (!Array.isArray(value)) fail(`${label} must be an array`);
  if (value.length > maxItems) fail(`${label} has too many entries`, { maxItems, actual: value.length });
  return value.map((item, index) => requireText(item, `${label}[${index}]`, { max }));
}

async function exactNodeByTitle(config, title) {
  const rows = await rpc(config, "logseq.DB.datascriptQuery", [
    `[:find (pull ?node [:db/id :block/uuid :block/title]) :where [?node :block/title ${JSON.stringify(title)}]]`,
  ]);
  const nodes = (Array.isArray(rows) ? rows : []).map((row) => Array.isArray(row) ? row[0] : row).filter(Boolean);
  if (nodes.length > 1) fail("Home OS found duplicate exact node titles", { title, matches: nodes.length });
  return nodes[0] ?? null;
}

async function ensurePage(config, title) {
  const page = await rpc(config, "logseq.Editor.getPage", [title]);
  if (page?.uuid) return { node: page, created: false };
  const collision = await exactNodeByTitle(config, title);
  if (collision) {
    fail("Home OS will not reuse a non-page node as a durable record", {
      title,
      uuid: collision.uuid,
      next: "Rename the existing block or choose a more specific Home OS record title.",
    });
  }
  const created = await rpc(config, "logseq.Editor.createPage", [title, {}, { createFirstBlock: false, redirect: false }]);
  const uuid = valueAt(created, ["uuid", "block/uuid", ":block/uuid"]);
  if (!uuid) fail("Logseq did not return a UUID for a created Home OS page", { title, response: created });
  const node = await rpc(config, "logseq.Editor.getPage", [uuid, { includeChildren: true }]);
  return { node, created: true };
}

