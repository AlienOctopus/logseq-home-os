function resolveCodexBinary() {
  const candidates = [
    process.env.CODEX_BINARY,
    "/Applications/ChatGPT.app/Contents/Resources/codex",
    "/Applications/Codex.app/Contents/Resources/codex",
  ].filter(Boolean);
  for (const candidate of candidates) if (fs.existsSync(candidate)) return candidate;
  const which = spawnSync("zsh", ["-lc", "command -v codex"], { encoding: "utf8" });
  if (which.status === 0 && which.stdout.trim()) return which.stdout.trim();
  fail("Could not locate the Codex CLI. Open or reinstall the ChatGPT desktop app.");
}

function sanitizeCodexReceipts(receipts, captures) {
  const allowedStatuses = new Set(["processed", "needs_review", "no_action", "blocked"]);
  const allowedReasonCodes = new Set([
    "processed",
    "needs-review",
    "no-action",
    "collision",
    "stale-capture",
    "graph-mismatch",
    "asset-unavailable",
    "api-error",
    "validation-error",
    "skill-unavailable",
    "unknown",
  ]);
  const expected = new Set(captures.map((capture) => capture.uuid));
  const byCapture = new Map();
  for (const receipt of receipts) {
    if (!receipt || !allowedStatuses.has(receipt.status) || !expected.has(receipt.capture_uuid)) continue;
    const recordUuid = typeof receipt.record_uuid === "string" && /^[0-9a-f-]{36}$/i.test(receipt.record_uuid)
      ? receipt.record_uuid
      : null;
    const reasonCode = allowedReasonCodes.has(receipt.reason_code) ? receipt.reason_code : "unknown";
    byCapture.set(receipt.capture_uuid, {
      status: receipt.status,
      capture_uuid: receipt.capture_uuid,
      record_uuid: recordUuid,
      reason_code: reasonCode,
    });
  }
  return [...byCapture.values()];
}

