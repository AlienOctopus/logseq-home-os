function loadStateForGraph(config) {
  const state = loadJson(STATE_PATH, { version: 2, graphs: {} });
  state.version = 2;
  state.graphs ??= {};
  const key = graphStateKey(config);
  if (!state.graphs[key] && state.handled) {
    state.graphs[key] = { handled: state.handled, lastSuccessfulRunAt: state.lastSuccessfulRunAt ?? null };
    delete state.handled;
    delete state.lastSuccessfulRunAt;
  }
  state.graphs[key] ??= { handled: {} };
  state.graphs[key].handled ??= {};
  return { state, graphState: state.graphs[key], key };
}

