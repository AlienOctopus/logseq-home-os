function readLogseqToken() {
  const envToken = process.env.LOGSEQ_API_TOKEN;
  if (envToken?.trim()) return envToken.trim().replace(/^Bearer\s+/i, "");
  let text;
  try {
    text = fs.readFileSync(LOGSEQ_CONFIG_PATH, "utf8");
  } catch (error) {
    fail("Could not read the Logseq desktop API configuration", { path: LOGSEQ_CONFIG_PATH, cause: error.message });
  }
  const match = text.match(/:server\/tokens\s+\[[\s\S]*?:value\s+"([^"]+)"/);
  if (!match) fail("No Logseq desktop API token was found", { path: LOGSEQ_CONFIG_PATH });
  return match[1];
}

async function rpc(config, method, args = []) {
  const controller = new AbortController();
  const timeout = setTimeout(() => controller.abort(), config.apiTimeoutMs ?? 15000);
  try {
    const response = await fetch(config.apiUrl ?? DEFAULT_API_URL, {
      method: "POST",
      headers: {
        Authorization: `Bearer ${readLogseqToken()}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({ method, args }),
      signal: controller.signal,
    });
    const text = await response.text();
    let payload;
    try { payload = text ? JSON.parse(text) : null; } catch { payload = text; }
    if (!response.ok) fail(`Logseq API returned HTTP ${response.status}`, payload);
    if (payload && typeof payload === "object" && payload.error) fail(`Logseq API method failed: ${method}`, payload.error);
    return payload;
  } finally {
    clearTimeout(timeout);
  }
}

function valueAt(object, keys) {
  if (!object || typeof object !== "object") return undefined;
  for (const key of keys) if (Object.hasOwn(object, key)) return object[key];
  return undefined;
}

function stable(value) {
  if (Array.isArray(value)) return value.map(stable);
  if (value && typeof value === "object") {
    return Object.fromEntries(Object.keys(value).sort().map((key) => [key, stable(value[key])]));
  }
  return value;
}

function fingerprint(value) {
  return createHash("sha256").update(JSON.stringify(stable(value))).digest("hex");
}

const VOLATILE_CAPTURE_KEYS = new Set([
  "id", "db/id", ":db/id", "createdAt", "updatedAt", "created-at", "updated-at",
  "block/created-at", ":block/created-at", "block/updated-at", ":block/updated-at",
  "collapsed", "collapsed?", "level", "order", "page", "parent", "refs",
  ":logseq.property/created-by-ref", "logseq.property/created-by-ref",
]);

function semanticCaptureValue(value) {
  if (Array.isArray(value)) return value.map(semanticCaptureValue);
  if (!value || typeof value !== "object") return value;
  const entries = [];
  for (const [key, item] of Object.entries(value)) {
    if (VOLATILE_CAPTURE_KEYS.has(key)) continue;
    entries.push([key, semanticCaptureValue(item)]);
  }
  return Object.fromEntries(entries);
}

function captureFingerprints(tree) {
  return {
    semantic: fingerprint(semanticCaptureValue(tree)),
    legacy: fingerprint(tree),
  };
}

function reconcileRunReceipts(captures, receipts, code) {
  const acceptedStatuses = new Set(["processed", "needs_review", "no_action"]);
  const expected = new Set(captures.map((capture) => capture.uuid));
  const acceptedByCapture = new Map();
  for (const receipt of receipts) {
    if (expected.has(receipt?.capture_uuid) && acceptedStatuses.has(receipt?.status)) {
      acceptedByCapture.set(receipt.capture_uuid, receipt);
    }
  }
  const handledCaptureUuids = code === 0 ? [...acceptedByCapture.keys()] : [];
  const missingCaptureUuids = captures.map((capture) => capture.uuid).filter((uuid) => !acceptedByCapture.has(uuid));
  return {
    ok: code === 0 && handledCaptureUuids.length > 0,
    complete: code === 0 && missingCaptureUuids.length === 0,
    handledCaptureUuids,
    missingCaptureUuids,
  };
}

