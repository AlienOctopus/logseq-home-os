  if (fs.existsSync(LAUNCH_AGENT_PATH)) fs.unlinkSync(LAUNCH_AGENT_PATH);
  const result = { ok: true, removed: LAUNCH_AGENT_PATH, label: LAUNCH_AGENT_LABEL, dataPreserved: true };
